#include <iostream>
using namespace std;
/**
Hacer un programa para mostrar por pantalla los números del 20 al 1 en orden decreciente.
Importante: El programa no tiene ningún ingreso de datos.


*/

int main()
{
    int i = 20;

    while(i>=1)
    {
        cout << i << endl;
        i--;

    }

    return 0;
}
