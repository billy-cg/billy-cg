#include <iostream>
using namespace std;
/**
   Hacer un programa para mostrar por pantalla los números del 1 al 10.
    Importante: El programa no tiene ningún ingreso de datos.


*/

int main()
{
    int i = 1;
    while(i <=10)
    {
        cout << i << endl;
        i++;
    }

    return 0;
}
